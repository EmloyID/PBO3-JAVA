public class Latihan13 {

    public static void main(String[] args) {
        
        //Variabel
        int bil1 = 10;
        int bil2 = 20;
        
        //Tampilan
        System.out.println("a == b = " + (bil1 == bil2));
        System.out.println("a != b = " + (bil1 != bil2));
        System.out.println("a > b = " + (bil1 > bil2));
        System.out.println("a < b = " + (bil1 < bil2));
        System.out.println("b >= a = " + (bil2 >= bil1));
        System.out.println("b <= a = " + (bil2 <= bil1));
    }
    
}
